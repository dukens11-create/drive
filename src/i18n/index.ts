/**
 * Translate a key for the given locale, falling back to English if the key is
 * missing from the target locale, and returning the key itself if it's missing
 * even from the fallback.
 */
export function t(key: string, locale: SupportedLocale = DEFAULT_LOCALE): string {
  return translations[locale]?.[key] ?? translations[DEFAULT_LOCALE]?.[key] ?? key;
}

/** Return all translation keys for a given locale (with English fallback). */
export function getTranslations(locale: SupportedLocale): TranslationMap {
  const base = translations[DEFAULT_LOCALE] ?? {};
  const overrides = translations[locale] ?? {};
  return { ...base, ...overrides };
}
