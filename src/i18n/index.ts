export const SUPPORTED_LOCALES = [
  'en', 'es', 'fr', 'de', 'it', 'pt', 'ru',
  'zh-CN', 'zh-TW', 'ja', 'ko', 'ar', 'hi',
  'th', 'tr', 'vi', 'id', 'nl', 'pl', 'sv',
] as const;

export type SupportedLocale = (typeof SUPPORTED_LOCALES)[number];

export const DEFAULT_LOCALE: SupportedLocale = 'en';

export const RTL_LOCALES = new Set<SupportedLocale>(['ar']);

export function isRTL(locale: SupportedLocale): boolean {
  return RTL_LOCALES.has(locale);
}

export function isSupportedLocale(value: string): value is SupportedLocale {
  return (SUPPORTED_LOCALES as readonly string[]).includes(value);
}

/** Resolve the best supported locale from an Accept-Language header value or locale string. */
export function resolveLocale(raw: string | undefined): SupportedLocale {
  if (!raw) return DEFAULT_LOCALE;

  const candidates = raw
    .split(',')
    .map((part) => {
      const [tag] = part.trim().split(';');
      return tag.trim();
    })
    .filter(Boolean);

  for (const candidate of candidates) {
    if (isSupportedLocale(candidate)) return candidate;
    const lang = candidate.split('-')[0];
    if (isSupportedLocale(lang as SupportedLocale)) return lang as SupportedLocale;
  }

  return DEFAULT_LOCALE;
}

